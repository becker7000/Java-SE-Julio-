public class Condicionales5 {

    public static void main(String[] args) {

        int valor = 0;

        if(valor<0){
            System.out.println("\n\t El número es negativo");
        }else if(valor>0){
            System.out.println("\n\t El número es positivo");
        }else{
            System.out.println("\n\t El número es cero");
        }

    }

}
